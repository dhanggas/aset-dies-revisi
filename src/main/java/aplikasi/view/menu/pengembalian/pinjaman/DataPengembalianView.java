    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        if (areAllNotEmpty(daftarPengembalianDeatil, txtKode.getText(), txtTanggal.getDateFormatString())) {
            try {
                pengembalian.setIdPeminjaman(Integer.valueOf(txtIdPeminjaman.getText()));
                pengembalian.setKode(txtKode.getText());
                pengembalian.setPembawa(txtPembawa.getText());
                pengembalian.setTanggal(Date.valueOf(ValueFormatter.getDateSQL(txtTanggal.getDate())));
                pengembalian.setKet(txtKet.getText());
                pengembalian.setUser(p);
                if (cboOpen.isSelected()) {
                    repoPengembalian.save(pengembalian, daftarPengembalianDeatil);
                    peminjaman.setOpen(false);
                    repoPeminjaman.update(peminjaman);
                } else {
                    repoPengembalian.save(pengembalian, daftarPengembalianDeatil);
                    peminjaman.setOpen(true);
                    repoPeminjaman.update(peminjaman);
                }

                JOptionPane.showMessageDialog(this, "Data Pengembalian berhasil disimpan", getTitle(), JOptionPane.INFORMATION_MESSAGE);

                DataPengembalianView view = new DataPengembalianView(menuController, p);
                this.menuController.setInnerLayout(view);
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, "Tidak dapat menyimpan data pengembalian", getTitle(), JOptionPane.ERROR_MESSAGE);
                Logger.getLogger(DataPengembalianView.class.getName()).log(Level.SEVERE, null, ex);
            } catch (PropertyVetoException ex) {
                JOptionPane.showMessageDialog(this, "Tidak dapat menampilkan form pengembalian yang baru", getTitle(), JOptionPane.ERROR_MESSAGE);
                Logger.getLogger(DataPengembalianView.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Data yang di masukkan belum lengkap !");

        }
    }//GEN-LAST:event_btnSimpanActionPerformed

