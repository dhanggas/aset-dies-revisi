    public void tambahAset(PengembalianDetail pd) {
        daftarPengembalianDeatil.add(pd);
        refreshDataTable();
    }

    public void updateAset(Integer index, PengembalianDetail pd) {
        daftarPengembalianDeatil.set(index, pd);
        refreshDataTable();
    }

    public void refreshDataTable() {
        tableController.clearData();
        int grantTotal = 0;
        for (PengembalianDetail balik : daftarPengembalianDeatil) {
            Aset aset = balik.getAset();
            Object[] row = {
                aset.getKode(),
                aset.getNama(),
                aset.getKategoriAset().getNama_kategori(),
                aset.getKepemilikan().getNama(),
                balik.getQty()
            };
            tableController.getModel().addRow(row);
            grantTotal += balik.getQty();
        }
        tableController.setContentTableAlignment(Arrays.asList(0, 2, 3, 4));
        txtTotal.setText(String.valueOf(grantTotal));
    }

    private void setField(Peminjaman pinjam) {
        try {
            txtIdPeminjaman.setText(pinjam.getId().toString());
            txtPembawa.setText(pinjam.getPembawa());
            txtTanggalPP.setText(ValueFormatter.getLocalDateLong(pinjam.getTanggal().toLocalDate()));
            txtKetPP.setText(pinjam.getKet());

            daftarPengembalianDeatil.clear();
            List<PeminjamanDetail> daftarAset = repoPeminjaman.findPeminjamanDetailByPeminjamanKode(pinjam.getKode());
            for (PeminjamanDetail pinjamDetail : daftarAset) {
                PengembalianDetail pd = new PengembalianDetail();
                pd.setAset(pinjamDetail.getAset());
                pd.setQty(pinjamDetail.getQty());
                pd.setPengembalian(this.pengembalian);
                daftarPengembalianDeatil.add(pd);
            }
            refreshDataTable();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Tidak dapat memuat data pinjaman pengembalian aset", getTitle(), JOptionPane.ERROR_MESSAGE);
            Logger.getLogger(DataPengembalianView.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void clearFields() {
        txtPembawa.setText("");
        txtTanggalPP.setText("");
        daftarPeminjaman.clear();
    }

