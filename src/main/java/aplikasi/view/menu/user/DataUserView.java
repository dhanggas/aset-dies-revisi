    private void textFieldLimit() {
        txtNama.setDocument(new FieldLimit(30));
        txtUsername.setDocument(new FieldLimit(8));
        txtPassword.setDocument(new FieldLimit(8));
    }

    private void clearFields() {
        txtKode.setText("");
        txtNama.setText("");
        txtUsername.setText("");
        txtPassword.setText("");
        txtJabatan.setSelectedIndex(1);
        txtStatus.setSelectedIndex(1);
    }

    private void setFilds(Users pengguna) {
        txtKode.setText(pengguna.getId_user().toString());
        txtNama.setText(pengguna.getNama());
        txtUsername.setText(pengguna.getUsername());
        txtPassword.setText(pengguna.getPassword());
        txtJabatan.setSelectedItem(pengguna.getJabatan().toString());
        if (pengguna.getStatus()) {
            txtStatus.setSelectedIndex(0);
        } else {
            txtStatus.setSelectedIndex(1);
        }
    }

    private void ambilIdTerbesar() {
        try {
            daftarUsers = repoUsers.findMaxValue();
            if (daftarUsers.size() != 0) {
                for (Users p : daftarUsers) {
                    int i = p.getId_user() + 1;
                    txtKode.setText(String.valueOf(i));
                }
            } else {
                txtKode.setText("1");
            }

        } catch (Exception e) {
            Logger.getLogger(DataUserView.class.getName()).log(Level.SEVERE, null, e);

        }
    }

