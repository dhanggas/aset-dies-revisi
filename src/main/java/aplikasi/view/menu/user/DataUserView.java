    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        if ((!jTextFieldMinimal.setMinInputValue(txtNama.getText(), 2) || !jTextFieldMinimal.setOnlyLetter(txtNama.getText()))) {
            JOptionPane.showMessageDialog(this, "Nama minimal 2 karakter dan hanya huruf !");
            txtNama.requestFocus();
            txtNama.selectAll();
        } else if (!jTextFieldMinimal.setMinInputValue(txtUsername.getText(), 4)) {
            JOptionPane.showMessageDialog(this, "Username minimal 4 karakter !");
            txtUsername.requestFocus();
            txtUsername.selectAll();
        } else if (!jTextFieldMinimal.setMinInputValue(txtPassword.getText())) {
            JOptionPane.showMessageDialog(this, "Password minimal 4 karakter, mengandung huruh besar, kecil dan angka !");
            txtPassword.requestFocus();
            txtPassword.selectAll();
        } else {
            pengguna.setId_user(Integer.valueOf(txtKode.getText()));
            pengguna.setNama(txtNama.getText());
            pengguna.setUsername(txtUsername.getText());
            pengguna.setPassword(txtPassword.getText());
            pengguna.setJabatan(Users.Role.valueOf(txtJabatan.getSelectedItem().toString()));
            if (txtStatus.getSelectedItem().equals("Aktif")) {
                pengguna.setStatus(true);
            } else {
                pengguna.setStatus(false);
            }

            if (isUpdate()) {
                try {
                    repoUsers.update(pengguna);
                    this.dispose();
                    if (daftarLokasiViewController != null) {
                        daftarLokasiViewController.refreshDataTableByName();
                        daftarLokasiViewController.setFields(pengguna);

                    }
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(this, "Username harus unique !", getTitle(), JOptionPane.ERROR_MESSAGE);
                    Logger.getLogger(DataUserView.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else {
                try {
                    repoUsers.save(pengguna);
                    this.dispose();
                    if (daftarLokasiViewController != null) {
                        daftarLokasiViewController.refreshDataTableByName();
                        daftarLokasiViewController.setFields(pengguna);
                        daftarLokasiViewController.selectLastRow();
                        daftarLokasiViewController.btnTambahRequestFocus();
                    }
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(this, "Username harus unique !", getTitle(), JOptionPane.ERROR_MESSAGE);
                    //                    JOptionPane.showMessageDialog(this, ex, getTitle(), JOptionPane.ERROR_MESSAGE);
                    Logger.getLogger(DataUserView.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

