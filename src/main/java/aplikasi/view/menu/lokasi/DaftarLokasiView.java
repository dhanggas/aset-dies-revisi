    public void refreshDataTable() {
        try {
            this.tableController.clearData();
            daftarLokasi = repo.findAll();
            for (LokasiAset k : daftarLokasi) {
                Object[] row = {k.getId_lokasi(), k.getNama_rak(), k.getLokasi()};
                tableController.getModel().addRow(row);
            }
            tableController.setContentTableAlignment(Arrays.asList(0, 1, 2));
        } catch (SQLException e) {
            Logger.getLogger(DaftarStatusView.class.getName()).log(Level.SEVERE, null, e);
        }
    }

    public void refreshDataTableByName() {
        try {
            this.tableController.clearData();
            daftarLokasi = repo.findByNama(txtCari.getText());
            for (LokasiAset k : daftarLokasi) {
                Object[] row = {k.getId_lokasi(), k.getNama_rak(), k.getLokasi()};
                tableController.getModel().addRow(row);
            }
        } catch (SQLException e) {
            Logger.getLogger(DaftarStatusView.class.getName()).log(Level.SEVERE, null, e);
        }
    }

